public class SomaNumeros {
  /*
  * Retorna a soma de dois números
  * @param numA um número inteiro
  * @param numB outro numero inteiro
  * @return a soma entre dois números
  */

  public static int retornaSomaEntreDoisNumeros(int numA, int numB) {
    int soma = numA + numB;
    return soma;
  }
}
